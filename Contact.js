class Contact {
  constructor(firstname, lastname, address, city, state, zip, phone, email) {
    this.firstname = firstname;
    this.lastname = lastname;
    this.address = address;
    this.city = city;
    this.state = state;
    this.zip = zip;
    this.phone = phone;
    this.email = email;
  }
}
module.exports = Contact;
